//Converts to Relative and multiplies the result by 10
func ConvertRawToRelative (Chain []RawCoordinates) []RawCoordinates {
    var (
	RC = make ([]RawCoordinates, len(Chain))
	X *p.Decimal
	Y *p.Decimal
	)
    for i:=0; i<len(Chain); i++ {
        if i == 0 {
            RC[i] = Chain[i]
	} else {
	    X = b.MULxc(b.SUBxc(Chain[i].X,Chain[i-1].X),p.NFS("10"))
	    Y = b.MULxc(b.SUBxc(Chain[i].Y,Chain[i-1].Y),p.NFS("10"))
	    RC[i].X = X
	    RC[i].Y = Y
	    RC[i].Type = Chain[i].Type
	    RC[i].Aux1 = Chain[i].Aux1
	    RC[i].Aux2 = Chain[i].Aux2
	    RC[i].Aux3 = Chain[i].Aux3
	}
    }
    return RC
}

func ConvertToVectorSyntax (Unit RawCoordinates) string {
    var OutputString string
    S1 := Unit.X.String()
    S2 := Unit.Y.String()
    if Unit.Type == "l" {
        OutputString = Unit.Type + S1 + "," + S2
    } else if Unit.Type == "a" {
        OutputString = Unit.Type + Unit.Aux1 + "," + Unit.Aux2 + " " + Unit.Aux3 + " " + S1 + "," + S2
    }
    return OutputString
}

func CreateSVGCode (Chain []RawCoordinates) string {
    var (
    	OutputCode string
    	UnitToAdd string
    )
    for i:=0; i<len(Chain); i++ {
        if i == len(Chain) - 1 {
            UnitToAdd = ConvertToVectorSyntax(Chain[i])
            OutputCode = OutputCode + UnitToAdd + " Z"
        }else {
            UnitToAdd = ConvertToVectorSyntax(Chain[i])
            OutputCode = OutputCode + UnitToAdd + " "
        }

    }

    return OutputCode
}

func CreateSVGCodeNoZ (Chain []RawCoordinates) string {
    var (
        OutputCode string
        UnitToAdd string
    )
    for i:=0; i<len(Chain); i++ {
        if i == len(Chain) - 1 {
            UnitToAdd = ConvertToVectorSyntax(Chain[i])
            OutputCode = OutputCode + UnitToAdd
        }else {
            UnitToAdd = ConvertToVectorSyntax(Chain[i])
            OutputCode = OutputCode + UnitToAdd + " "
        }

    }

    return OutputCode
}